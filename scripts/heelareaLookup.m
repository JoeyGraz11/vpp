function Sh = heelareaLookup(B)
% PYD Table 5.26
T =[5.0, -4.112, 0.054, -0.027, 6.329;...
    10.0, -4.522, -0.132, -0.077, 8.738;...
    15.0, -3.291, -0.389, -0.118, 8.949;...
    20.0, 1.8500, -1.2, -0.109, 5.364;...
    25.0, 6.5100, -2.305, -0.066, 3.443;...
    30.0, 12.334, -3.911, 0.024, 1.767;...
    35.0, 14.648, -5.182, 0.102, 3.497];

heel = B.heel;
S = B.Sc;
Beam = B.Bwl;
Tc = B.Tc;
Cm = B.Cm;

phi = T(:, 1);
s0 = pchip(phi, T(:, 2), heel);
s1 = pchip(phi, T(:, 3), heel);
s2 = pchip(phi, T(:, 4), heel);
s3 = pchip(phi, T(:, 5), heel);

Sh = S*(1 + 0.01*(s0 + s1*(Beam/Tc) + s2*(Beam/Tc)^2 + s3*Cm));
end